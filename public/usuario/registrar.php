	<p>Pensando em possibilitar aos nossos clientes um número ilimitado de cadastros, criamos o plano cliente ouro. Nesse, o cliente cadastra quantos produtos achar necessário. Sem limite nenhum de acessos. Gerando um custo mensal de R$ 600,00 (Seiscentos Reais).</p>

